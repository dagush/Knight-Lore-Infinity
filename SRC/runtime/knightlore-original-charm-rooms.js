const unpackPosition = value => ({
    x: value & 0x07,
    y: (value >> 3) & 0x07,
    z: (value >> 6) & 0x03,
});

const decodedTemplates = RAW_ORIGINAL_CHARM_ROOM_INTERIORS.map((entry, index) => {
    const [originalRoomId, spawn, runs] = entry;
    return {
        index,
        originalRoomId,
        originalRoomHex: `0x${originalRoomId.toString(16).toUpperCase().padStart(2, '0')}`,
        spawn: {x: spawn[0], y: spawn[1], z: spawn[2]},
        blocks: runs.map(([type, positions]) => ({
            type,
            positions: positions.map(unpackPosition),
        })),
        source: 'Knight Lore original special_objs_tbl + location_tbl interior block groups',
        architecturePolicy: 'generated exits, walls, size, colour, and theme remain authoritative',
    };
});

export const ORIGINAL_CHARM_ROOM_INTERIOR_COUNT = decodedTemplates.length;

export const getOriginalCharmRoomInterior = index => {
    const normalized = ((Number(index) % decodedTemplates.length) + decodedTemplates.length) % decodedTemplates.length;
    return JSON.parse(JSON.stringify(decodedTemplates[normalized]));
};

export const getOriginalCharmRoomInteriorByRoomId = roomId => {
    const normalized = Number(roomId) & 0xff;
    const template = decodedTemplates.find(item => item.originalRoomId === normalized);
    return template ? JSON.parse(JSON.stringify(template)) : null;
};
